#Escribir un programa que lea dos números enteros y los compare para determinar cuál es el mayor y cuál es el menor.

num_1= int(input("Ingrese un número: "))
num_2= int(input("Ingrese un número: "))

if num_1 > num_2:
    print("Este número es mayor")
if num_1 < num_2:
    print("Este número es menor")
